import java.util.Scanner;
public class AVP_of_Cube{
    public static void main(String[]args){
        Scanner sc=new Scanner(System.in);
        int s=sc.nextInt();
        int a=6*s*s;
        int v=s*s*s;
        int p=12*s;
        System.out.println(a);
        System.out.println(v);
        System.out.println(p);
    }
}